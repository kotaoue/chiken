package portrait

import "errors"

const (
	BasicStyle     = "basic"
	WalkStyle      = "walk"
	WideStyle      = "wide"
	TiptoeStyle    = "tiptoe"
	JumpStyle      = "jump"
	SleepStyle     = "sleep"
	DeepSleepStyle = "deepSleep"
	WakeStyle      = "wake"
)

type Style struct{}

func (s Style) Get(style string) ([][]int, error) {
	switch style {
	case BasicStyle:
		return s.basic(), nil
	case WalkStyle:
		return s.walk(), nil
	case WideStyle:
		return s.wide(), nil
	case TiptoeStyle:
		return s.tiptoe(), nil
	case JumpStyle:
		return s.jump(), nil
	case SleepStyle:
		return s.sleep(), nil
	case DeepSleepStyle:
		return s.deepSleep(), nil
	case WakeStyle:
		return s.wake(), nil
	}
	return nil, errors.New("not exist style")
}

